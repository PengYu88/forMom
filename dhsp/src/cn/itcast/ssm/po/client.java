package cn.itcast.ssm.po;

public class client {

	private String clientId;
	
	private String clientCode;
	
	private String clientName;
	
	private String clientAddress;
	
	private String clientTelephone;
	
	private String logisticsArrival;
	
	private String district;
	
	private String pickupAdress;

	public String getClientId() {
		return clientId;
	}

	public void setClientId(String clientId) {
		this.clientId = clientId;
	}

	public String getClientCode() {
		return clientCode;
	}

	public void setClientCode(String clientCode) {
		this.clientCode = clientCode;
	}

	public String getClientName() {
		return clientName;
	}

	public void setClientName(String clientName) {
		this.clientName = clientName;
	}

	public String getClientAddress() {
		return clientAddress;
	}

	public void setClientAddress(String clientAddress) {
		this.clientAddress = clientAddress;
	}

	public String getClientTelephone() {
		return clientTelephone;
	}

	public void setClientTelephone(String clientTelephone) {
		this.clientTelephone = clientTelephone;
	}

	public String getLogisticsArrival() {
		return logisticsArrival;
	}

	public void setLogisticsArrival(String logisticsArrival) {
		this.logisticsArrival = logisticsArrival;
	}

	public String getDistrict() {
		return district;
	}

	public void setDistrict(String district) {
		this.district = district;
	}

	public String getPickupAdress() {
		return pickupAdress;
	}

	public void setPickupAdress(String pickupAdress) {
		this.pickupAdress = pickupAdress;
	}
	
	
	
}